import {
  Nunito_200ExtraLight,
  Nunito_200ExtraLight_Italic,
  Nunito_300Light,
  Nunito_300Light_Italic,
  Nunito_400Regular,
  Nunito_400Regular_Italic,
  Nunito_500Medium,
  Nunito_500Medium_Italic,
  Nunito_600SemiBold,
  Nunito_600SemiBold_Italic,
  Nunito_700Bold,
  Nunito_700Bold_Italic,
  Nunito_800ExtraBold,
  Nunito_800ExtraBold_Italic,
  Nunito_900Black,
  Nunito_900Black_Italic,
} from '@expo-google-fonts/nunito';
import { useFonts } from 'expo-font';
import * as Location from 'expo-location';
import * as SplashScreen from 'expo-splash-screen';
import { StatusBar } from 'expo-status-bar';
import { useCallback } from 'react';
import { LogBox, View } from 'react-native';
import 'react-native-gesture-handler';
import { PrincipalContextProvider } from './modules/shared/PrincipalContext';
import { GlobalNavigator } from './sections/global-navigator';
SplashScreen.preventAutoHideAsync();
LogBox.ignoreLogs(['new NativeEventEmitter']);
Location.requestBackgroundPermissionsAsync();
Location.requestForegroundPermissionsAsync();
export default function App() {
  const [fontsLoaded, fontsError] = useFonts({
    Nunito_200ExtraLight,
    Nunito_300Light,
    Nunito_400Regular,
    Nunito_500Medium,
    Nunito_600SemiBold,
    Nunito_700Bold,
    Nunito_800ExtraBold,
    Nunito_900Black,
    Nunito_200ExtraLight_Italic,
    Nunito_300Light_Italic,
    Nunito_400Regular_Italic,
    Nunito_500Medium_Italic,
    Nunito_600SemiBold_Italic,
    Nunito_700Bold_Italic,
    Nunito_800ExtraBold_Italic,
    Nunito_900Black_Italic,
  });
  const onLayoutRootView = useCallback(async () => {
    if (fontsLoaded || fontsError) {
      await SplashScreen.hideAsync();
    }
  }, [fontsLoaded, fontsError]);

  if (!fontsLoaded && !fontsError) {
    return null;
  }
  return (
    <>
      <PrincipalContextProvider
        configurations={{
          baseUrl: 'http://localhost:3001',
          citiesUrl: 'https://countriesnow.space/api/v0.1/countries/cities',
          countriesUrl: 'https://restcountries.com/v3.1/all',
        }}
      >
        <GlobalNavigator></GlobalNavigator>
      </PrincipalContextProvider>
      <View style={{ backgroundColor: 'transparent' }} onLayout={onLayoutRootView}></View>
      <StatusBar style="dark" translucent />
    </>
  );
}
